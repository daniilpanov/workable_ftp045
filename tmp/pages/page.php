<p></p>
<span class="excl blue">Нормы при строительстве второго уровня</span>
<p>
    Специалисты рекомендуют доверять изготовление и монтаж второго уровня только профессионалам,
    которые гарантируют безопасность конструкции и сохранение комфортных условий.
<p>
    Следующий момент, который нельзя упускать из внимания — вентиляция воздуха наверху.
    Горячий воздух легче холодного, поэтому у потолка всегда душно.
    Решить эту проблему может дополнительный канал вентиляции или хороший потолочный вентилятор.
    Если есть небольшое окно, то вопрос с проветриванием решается проще.
<p>
    Если высота потолка позволяет встать в полный рост, то <b>балюстрада</b> по высоте должна доходить до пояса,
    т. е. Быть выше центра тяжести (где-то 1 метр в высоту). Такая норма обязательна, когда есть дети.
<p>
    На этапе планирования рассчитывают общую нагрузку на основание.
    В зависимости от этого выбирают способ крепления и материалы.
    Изготовление антресоли и <b>лестницы</b> к ней выполняется обычно <b>по индивидуальному проекту</b>,
    который учитывает все Ваши пожелания.

