<p>
    Уникальный дизайн антресоли передают с помощью <b>балюстрады (ограждения балкона)</b> и лестницы.
    Балюстраду изготавливают <b>из дерева, кованное, из нержавеющей стали,
        стекла триплекс закаленного</b> или комбинированного материала.
    Выбор материала зависит от стиля и обстановки в комнате. Сплошное стекло, например, облегчает всю конструкцию,
    металл подчеркивает стиль Hi tech, дерево вписывается в классический стиль.
<p>
    Для экономия места часто используют <b>лестницы</b> с крутым подъемом, более 45 градусов.
    Чтобы такая лестница была безопасной, ее делают с так называемым «<b>гусиным шагом</b>».
<p>
    Ступени такой лестницы имеют нестандартную форму – косые или овальные вырезы.
    На них полноценно может уместиться лишь ступня одной ноги.
    Подходит для установки в ограниченном пространстве.
    Так же часто используют винтовые лестницы. Такая лестница, чем у нее больше радиус, тем комфортней подъем.
